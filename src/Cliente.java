public class Cliente {

    String nome;
    int cpf;
    String endereco;
    int idade;
    char sexo;

    public Cliente(String nome, int cpf, String endereco, int idade, char sexo){

        this.nome = nome;
        this.cpf = cpf;
        this.endereco = endereco;
        this.idade = idade;
        this.sexo = sexo;
    }

}
