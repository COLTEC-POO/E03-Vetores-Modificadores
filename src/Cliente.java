public class Cliente {
    String nome;
    String CPF;
    String endereço;
    int idade;
    char sexo;

        // Método construtor da classe Cliente

        Cliente (String nome, String CPF, String endereço, int idade, char sexo) {
            this.nome = nome;
            this.CPF = CPF;
            this.endereço = endereço;
            this.idade = idade;
            this.sexo = sexo;        
    }
}